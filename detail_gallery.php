<h2>Кондитерская фабрика "Сладко", г. Екатеринбург</h2>
<p>
Нашей компанией была произведена отгрузка дымовых труб - Ду =900 мм и Н=30 000мм для котельной общей мощностью 180 МВт. На данный момент запущена в работу первая очередь котельной на 20МВт. Данный объект обеспечивает теплом весь жилой фонд микрорайона Солнечный Чкаловского района города Екатеринбург.
